def categories_keyboard() -> InlineKeyboardMarkup:
    buttons = [
        [InlineKeyboardButton(
            text=f"{data['emoji']} {cat}",
            callback_data=f"faq_cat:{cat}"
        )]
        for cat, data in FAQ.items()
    ]
    return InlineKeyboardMarkup(inline_keyboard=buttons)


def questions_keyboard(category: str) -> InlineKeyboardMarkup:
    items = FAQ[category]["items"]
    buttons = [
        [InlineKeyboardButton(
            text=f"❓ {item['q'][:45]}{'...' if len(item['q'])>45 else ''}",
            callback_data=f"faq_q:{category}:{i}"
        )]
        for i, item in enumerate(items)
    ]
    buttons.append([InlineKeyboardButton(text="◀️ До категорій", callback_data="faq_back")])
    return InlineKeyboardMarkup(inline_keyboard=buttons)


def back_keyboard(category: str) -> InlineKeyboardMarkup:
    return InlineKeyboardMarkup(inline_keyboard=[
        [InlineKeyboardButton(text="◀️ Назад до питань", callback_data=f"faq_cat:{category}")],
        [InlineKeyboardButton(text="🏠 До категорій", callback_data="faq_back")],
    ])


@router.message(F.text == "💬 FAQ")
async def faq_menu(message: Message):
    total = sum(len(v["items"]) for v in FAQ.values())
    await message.answer(
        f"💬 Питання та відповіді для студентів ТНТУ\n\n"
        f"Всього відповідей: {total}\n\n"
        "Обери категорію:",
        reply_markup=categories_keyboard()
    )


@router.callback_query(F.data == "faq_back")
async def faq_back(callback: CallbackQuery):
    total = sum(len(v["items"]) for v in FAQ.values())
    await callback.message.edit_text(
        f"💬 Питання та відповіді для студентів ТНТУ\n\nВсього відповідей: {total}\n\nОбери категорію:",
        reply_markup=categories_keyboard()
    )
    await callback.answer()


@router.callback_query(F.data.startswith("faq_cat:"))
async def faq_category(callback: CallbackQuery):
    category = callback.data.split(":", 1)[1]
    if category not in FAQ:
        await callback.answer("❌ Не знайдено")
        return

    data = FAQ[category]
    await callback.message.edit_text(
        f"{data['emoji']} {category}\n\nОбери питання:",
        reply_markup=questions_keyboard(category)
    )
    await callback.answer()


@router.callback_query(F.data.startswith("faq_q:"))
async def faq_answer(callback: CallbackQuery):
    _, category, idx_str = callback.data.split(":", 2)
    idx = int(idx_str)

    if category not in FAQ or idx >= len(FAQ[category]["items"]):
        await callback.answer("❌ Не знайдено")
        return

    item = FAQ[category]["items"][idx]
    await callback.message.edit_text(
        f"❓ {item['q']}\n\n{item['a']}",
        reply_markup=back_keyboard(category)
    )
    await callback.answer()