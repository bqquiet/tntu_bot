def categories_keyboard() -> InlineKeyboardMarkup:
    return InlineKeyboardMarkup(inline_keyboard=[
        [InlineKeyboardButton(text=cat, callback_data=f"faq:{cat}")]
        for cat in FAQ
    ])


def questions_keyboard(cat: str) -> InlineKeyboardMarkup:
    buttons = [
        [InlineKeyboardButton(
            text=f"{''.join(q[:50])}{'...' if len(q)>50 else ''}",
            callback_data=f"faqq:{cat}:{i}"
        )]
        for i, (q, _) in enumerate(FAQ[cat])
    ]
    buttons.append([InlineKeyboardButton(text="◀️ Категорії", callback_data="faq:back")])
    return InlineKeyboardMarkup(inline_keyboard=buttons)


def answer_keyboard(cat: str) -> InlineKeyboardMarkup:
    return InlineKeyboardMarkup(inline_keyboard=[
        [InlineKeyboardButton(text="◀️ Назад до питань", callback_data=f"faq:{cat}")],
        [InlineKeyboardButton(text="🏠 Всі категорії", callback_data="faq:back")],
    ])


@router.message(F.text == "💬 FAQ")
async def faq_menu(message: Message):
    total = sum(len(v) for v in FAQ.values())
    await message.answer(
        f"💬 Питання та відповіді для студентів ТНТУ\n\n"
        f"Всього відповідей: {total}\n\n"
        "Обери категорію:",
        reply_markup=categories_keyboard()
    )


@router.callback_query(F.data == "faq:back")
async def faq_back(callback: CallbackQuery):
    total = sum(len(v) for v in FAQ.values())
    await callback.message.edit_text(
        f"💬 Питання та відповіді для студентів ТНТУ\n\nВсього відповідей: {total}\n\nОбери категорію:",
        reply_markup=categories_keyboard()
    )
    await callback.answer()


@router.callback_query(F.data.startswith("faq:"))
async def faq_category(callback: CallbackQuery):
    cat = callback.data.split(":", 1)[1]
    if cat == "back":
        return
    if cat not in FAQ:
        await callback.answer("❌ Не знайдено")
        return
    await callback.message.edit_text(
        f"{cat}\n\nОбери питання:",
        reply_markup=questions_keyboard(cat)
    )
    await callback.answer()


@router.callback_query(F.data.startswith("faqq:"))
async def faq_answer(callback: CallbackQuery):
    parts = callback.data.split(":", 2)
    cat = parts[1]
    idx = int(parts[2])
    if cat not in FAQ or idx >= len(FAQ[cat]):
        await callback.answer("❌ Не знайдено")
        return
    question, answer = FAQ[cat][idx]
    await callback.message.edit_text(
        f"❓ {question}\n\n{answer}",
        reply_markup=answer_keyboard(cat)
    )
    await callback.answer()